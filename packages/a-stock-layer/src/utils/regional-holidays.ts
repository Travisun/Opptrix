const CACHE = new Map<string, Set<string>>()

export function regionalHolidaysForYear(market: RegionalEquityMarket, year: number): Set<string> {
  const key = `${market}:${year}`
  const hit = CACHE.get(key)
  if (hit) return hit
  const rows = STATIC[market]?.[year] ?? []
  const set = new Set(rows)
  CACHE.set(key, set)
  return set
}

export function isRegionalHoliday(market: RegionalEquityMarket, dateStr: string): boolean {
  const year = Number(dateStr.slice(0, 4))
  if (!Number.isFinite(year)) return false
  return regionalHolidaysForYear(market, year).has(dateStr)
}
